package com.cj_wrong_data_searching.vo;

public class WrongDataVO {
	
	private String category;
	private String sku_title;
	private String title_txt;
	private String opt_txt;
	private String unit_count;
	private String group_count;
	private String unit_price;
	private String market_pid;
	private String addr;
	
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	
	
	public String getUnit_count() {
		return unit_count;
	}
	public void setUnit_count(String unit_count) {
		this.unit_count = unit_count;
	}
	
	
	public String getGroup_count() {
		return group_count;
	}
	public void setGroup_count(String group_count) {
		this.group_count = group_count;
	}
	
	
	public String getSku_title() {
		return sku_title;
	}
	public void setSku_title(String sku_title) {
		this.sku_title = sku_title;
	}
	
	
	public String getTitle_txt() {
		return title_txt;
	}
	public void setTitle_txt(String title_txt) {
		this.title_txt = title_txt;
	}
	
	
	public String getOpt_txt() {
		return opt_txt;
	}
	public void setOpt_txt(String opt_txt) {
		this.opt_txt = opt_txt;
	}
	
	
	public String getUnit_price() {
		return unit_price;
	}
	public void setUnit_price(String unit_price) {
		this.unit_price = unit_price;
	}
	
	
	public String getMarket_pid() {
		return market_pid;
	}
	public void setMarket_pid(String market_pid) {
		this.market_pid = market_pid;
	}
	
	
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	
}
